package com.nima.render.model;

/**
 *
 */
public class JsonLayerObjects {
  private int height;
  private int id;
  private String name;
  private String type;
  private boolean visible;
  private int width;
  private int x;
  private int y;

  public int getHeight() {
    return height;
  }

  public int getId() {
    return id;
  }

  public String getName() {
    return name;
  }

  public String getType() {
    return type;
  }

  public boolean isVisible() {
    return visible;
  }

  public int getWidth() {
    return width;
  }

  public int getX() {
    return x;
  }

  public int getY() {
    return y;
  }
}
